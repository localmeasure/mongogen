package mongogen

import (
	"bytes"
	"fmt"

	"github.com/jinzhu/inflection"
)

type Generator struct {
	buf    bytes.Buffer
	indent string
}

func NewGenerator() *Generator {
	return &Generator{}
}

func (g *Generator) p(format string, args ...interface{}) {
	fmt.Fprintf(&g.buf, g.indent+format+"\n", args...)
}

func (g *Generator) in() {
	g.indent += "\t"
}

func (g *Generator) out() {
	if len(g.indent) > 0 {
		g.indent = g.indent[0 : len(g.indent)-1]
	}
}

func (g *Generator) Output() []byte {
	return g.buf.Bytes()
}

type pkg struct {
	imports map[string]struct{}
	indexes []index
}

func (g *Generator) Gen(pkgName string, collection string, indexes []string) {
	public := toCamelCase(collection, true)
	singular := inflection.Singular(public)
	plural := inflection.Plural(public)
	pkg := analyze(indexes, singular)
	filterTyp := singular + "Filter"
	g.p("// Code generated by MongoGen. DO NOT EDIT.")
	g.p("// Collection: %s", collection)
	g.p("")
	g.p("package %s", pkgName)
	g.p("")
	g.p("import (")
	g.in()
	for path := range pkg.imports {
		g.p("%q", path)
	}
	g.out()
	g.p(")")
	g.p("")

	g.p("type %s struct {", filterTyp)
	g.in()
	g.p("Filter bson.D")
	g.out()
	g.p("}")
	g.p("")

	g.p("type %s struct {", plural)
	g.in()
	g.p("db *mongo.Database")
	g.out()
	g.p("}")
	g.p("")

	g.p("func NewService(db *mongo.Database) *%s {", plural)
	g.in()
	g.p("return &%s{db}", plural)
	g.out()
	g.p("}")
	g.p("")

	g.p("func %sWithID(id primitive.ObjectID) %s {", singular, filterTyp)
	g.in()
	g.p("return %s{bson.D{{Key: %q, Value: %s}}}", filterTyp, "_id", "id")
	g.out()
	g.p("}")
	g.p("")

	g.p("func %sWithIDs(ids []primitive.ObjectID) %s {", singular, filterTyp)
	g.in()
	g.p("return %s{bson.D{{Key: %q, Value: bson.M{%q: ids}}}}", filterTyp, "_id", "$in")
	g.out()
	g.p("}")
	g.p("")

	for _, idx := range pkg.indexes {
		printIdxTyp(g, idx)
		g.p("")
		printIdxFn(g, idx)
		g.p("")
		printIdxFilter(g, idx, filterTyp)
		g.p("")
		for _, k := range idx.keys {
			ops, ok := typOps[k.typ]
			if ok {
				printOps(g, ops, k, idx.name)
			}
		}
	}

	g.p("func (s *%s) Find(ctx context.Context, filter %sFilter, opts ...*options.FindOptions) (*mongo.Cursor, error) {", plural, singular)
	g.in()
	g.p("return s.db.Collection(%q).Find(ctx, filter.Filter, opts...)", collection)
	g.out()
	g.p("}")
	g.p("")

	g.p("func (s *%s) FindWithIDs(ctx context.Context, ids []primitive.ObjectID, opts ...*options.FindOptions) (*mongo.Cursor, error) {", plural)
	g.in()
	g.p("return s.db.Collection(%q).Find(ctx, bson.M{%q: bson.M{%q: ids}}, opts...)", collection, "_id", "$in")
	g.out()
	g.p("}")
	g.p("")

	g.p("func (s *%s) FindOne(ctx context.Context, filter %sFilter, opts ...*options.FindOneOptions) *mongo.SingleResult {", plural, singular)
	g.in()
	g.p("return s.db.Collection(%q).FindOne(ctx, filter.Filter, opts...)", collection)
	g.out()
	g.p("}")
	g.p("")

	g.p("func (s *%s) FindOneWithID(ctx context.Context, id primitive.ObjectID, opts ...*options.FindOneOptions) *mongo.SingleResult {", plural)
	g.in()
	g.p("return s.db.Collection(%q).FindOne(ctx, bson.M{%q: id}, opts...)", collection, "_id")
	g.out()
	g.p("}")
	g.p("")

	g.p("func (s *%s) Count(ctx context.Context, filter %sFilter, opts ...*options.CountOptions) (int64, error) {", plural, singular)
	g.in()
	g.p("return s.db.Collection(%q).CountDocuments(ctx, filter.Filter, opts...)", collection)
	g.out()
	g.p("}")
}

func printIdxTyp(g *Generator, idx index) {
	g.p("type %s struct {", idx.name)
	g.in()
	for _, k := range idx.keys {
		g.p("%-24sbson.M", k.goname)
	}
	g.out()
	g.p("}")
}

func printIdxFn(g *Generator, idx index) {
	fnName := toCamelCase(idx.name, true)
	g.p("func %s() *%s {", fnName, idx.name)
	g.in()
	g.p("return &%s{%s: bson.M{%q: 0}}", idx.name, idx.keys[0].goname, "$eq")
	g.out()
	g.p("}")
}

func printIdxFilter(g *Generator, idx index, filterTyp string) {
	g.p("func (use *%s) Build() %s {", idx.name, filterTyp)
	g.in()
	g.p("filter := bson.D{primitive.E{Key: %q, Value: use.%s}}", idx.keys[0].name, idx.keys[0].goname)
	for i := 1; i < len(idx.keys); i++ {
		g.p("if use.%s != nil {", idx.keys[i].goname)
		g.in()
		g.p("filter = append(filter, primitive.E{Key: %q, Value: use.%s})", idx.keys[0].name, idx.keys[0].goname)
		g.out()
		g.p("}")
	}
	g.p("return %s{filter}", filterTyp)
	g.out()
	g.p("}")
}

func printOps(g *Generator, ops []string, key indexKey, idxName string) {
	for _, op := range ops {
		switch op {
		case "eq":
			printEq(g, key, idxName)
		case "ne":
			printNe(g, key, idxName)
		case "in":
			printIn(g, key, idxName)
		case "nin":
			printNin(g, key, idxName)
		case "gt":
			printGt(g, key, idxName)
		case "gte":
			printGte(g, key, idxName)
		case "lt":
			printLt(g, key, idxName)
		case "lte":
			printLte(g, key, idxName)
		case "all":
			printAll(g, key, idxName)
		}
		g.p("")
	}
}

func printEq(g *Generator, key indexKey, idxName string) {
	fnName := "With" + toCamelCase(key.goname, true)
	g.p("func (use *%s) %s(value %s) *%s {", idxName, fnName, key.typ, idxName)
	g.in()
	g.p("use.%s = bson.M{%q: value}", key.goname, "$eq")
	g.p("return use")
	g.out()
	g.p("}")
}

